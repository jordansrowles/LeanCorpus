            ("яла", ""), ("али", ""), ("яли", ""), ("ать", ""),
            ("ять", ""), ("ить", ""),
        ],
        // Step 5: Noun endings
        [
            ("ости", ""), ("ость", ""), ("ений", ""), ("ения", ""),
            ("ению", ""), ("ение", ""), ("аний", ""), ("ания", ""),
            ("анию", ""), ("ание", ""),
